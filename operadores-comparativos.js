/*
 * > - Maior
 * < - Menor
 * >= - Maior ou igual
 * <= - Menor ou igual
 * == - Igual
 * != - Diferente
 */

console.log(20 > 10); //Maior que 
console.log(20+10 > 50); //Maior que com operação matemática
console.log(10<=2);//Menor ou igual
console.log(20 == 10*2);//Igual
console.log(150 != 3*50);//Diferente